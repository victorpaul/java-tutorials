public class NumberMathOperations {

    public static void main(String[] args){
        int aInt = 9;
        int bInt = 2;
        int div1 = aInt/bInt;
        System.out.println(aInt + "/" + bInt+ "=" + div1);

       float aFloat = 9;
        float bFloat = 2;
        float div2 = aFloat/bFloat;
        System.out.println(aFloat + "/" + bFloat+ "=" + div2);

        int plus1 = aInt+bInt;
        System.out.println(aInt + "+" + bInt+ "=" + plus1);

        float plus2 = aFloat+bFloat;
        System.out.println(aFloat + "+" + bFloat+ "="  + plus2);
    }
}